function applyModule(module, spec) {
  const lesson = module.lessons?.[0];
  if (!lesson || !Array.isArray(lesson.pages)) {
    throw new Error(`Lesson pages not found for module: ${module.title}`);
  }

  lesson.title = spec.title;

  pagePlan(spec).forEach((plan, index) => {
    const pageNo = String(index + 1).padStart(2, "0");
    const page = lesson.pages.find((entry) => String(entry.id).startsWith(`${module.id}-p${pageNo}-`));
    if (!page) throw new Error(`Expected page not found for ${module.title}: p${pageNo}`);
    const title = `${spec.title}: ${plan.suffix}`;
    page.title = title;
    page.left = left(title, plan.suffix, plan.paragraphs, spec);
    page.workspace = {
      layout: plan.layout,
      blocks: [plan.block(page)]
    };
  });
}

function applyContainer(target) {
  const filePath = path.join(contentRoot, target.slug, "container.json");
  const container = JSON.parse(fs.readFileSync(filePath, "utf8"));
  container.updatedAt = updatedAt;

  target.modules.forEach((spec) => {
    const module = container.modules.find((entry) => cleanTitle(entry.title) === spec.title);
    if (!module) throw new Error(`Module not found in ${target.slug}: ${spec.title}`);
    applyModule(module, spec);
  });

  fs.writeFileSync(filePath, `${JSON.stringify(container, null, 2)}\n`, "utf8");
  return filePath;
}

